#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_fputstr(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_fputstr(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		n += '0';
		ft_fputstr(fd, (const char *)&n, 1);
	}
}
