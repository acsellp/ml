#include "libft.h"

int			ft_atoi(const char *s)
{
	int		res;
	int		r;

	while (ft_iswspace(*s))
		s++;
	r = 1;
	if (*s == '-')
		r = -1;
	if (*s == '-' || *s == '+')
		s++;
	res = 0;
	while (*s >= '0' && *s <= '9')
	{
		res = (res * 10) + (*s - '0');
		s++;
	}
	return (res * r);
}
